# Native language names for each locale
LANGUAGE_NAMES = {
    "en_US": "English",
    "zh_CN": "简体中文",
    "zh_TW": "繁體中文",
}


def tr(key, locale="en_US"):
    """Get translation for a key."""
    return TRANSLATIONS.get(locale, TRANSLATIONS["en_US"]).get(key, key)


def get_language_name(locale):
    """Get the native name for a locale."""
    return LANGUAGE_NAMES.get(locale, locale)

